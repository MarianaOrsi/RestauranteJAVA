public class Bebidas {
    String nomeBebida;
    boolean alcoolico;
    double porcao, kcal, valor;

    public Bebidas(String nomeBebida, boolean alcoolico, double porcao, double kcal, double valor) {
        this.nomeBebida = nomeBebida;
        this.alcoolico = alcoolico;
        this.porcao = porcao;
        this.kcal = kcal;
        this.valor = valor;
    }




}
